/**
 * @param {string} str
 * @returns {string}
 */
export function normalizeJapanese(str) {
  if (typeof str !== 'string' || str.length === 0) return str;
  let normalized = str;
  for (const [from, to] of REPLACEMENTS) {
    normalized = normalized.split(from).join(to);
  }
  return normalized;
}
